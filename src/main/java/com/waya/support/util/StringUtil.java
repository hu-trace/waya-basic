	private static final String FOLDER_SEPARATOR = "/";
	private static final char EXTENSION_SEPARATOR = '.';
	private static final String CHARSET_UTF8 = "UTF-8";
	
	/**
	 * 将{@link Map}转换成queryString字符串，并根据参数名按照ASCII码从小到大排序
	 * @param paraMap {@link Map}
	 * @return queryString
	 */
	public static String fromMap(Map<String, Object> paraMap) {
		String buff = "";
		Map<String, Object> tmpMap = paraMap;
		try {
			List<Map.Entry<String, Object>> infoIds = new ArrayList<Map.Entry<String, Object>>(
					tmpMap.entrySet());
			// 对所有传入参数按照字段名的 ASCII 码从小到大排序（字典序）
			Collections.sort(infoIds,
					new Comparator<Map.Entry<String, Object>>() {
						@Override
						public int compare(Map.Entry<String, Object> o1,
								Map.Entry<String, Object> o2) {
							return (o1.getKey()).toString().compareTo(
									o2.getKey());
						}
					});
			// 构造URL 键值对的格式
			StringBuilder buf = new StringBuilder();
			for (Map.Entry<String, Object> item : infoIds) {
				if (!StringUtil.isEmpty(item.getKey())) {
					String key = item.getKey();
					Object val = item.getValue();
					try {
						buf.append(key + "=" + URLEncoder.encode(val.toString(), CHARSET_UTF8));
					}catch (Exception e) {
						buf.append(key + "=" + val);
					}
					buf.append("&");
				}

			}
			buff = buf.toString();
			if (buff.isEmpty() == false) {
				buff = buff.substring(0, buff.length() - 1);
			}
		} catch (Exception e) {
			e.printStackTrace();
			return null;
		}
		return buff;
	}
	
	/**
	 * 将{@link JSONObject}转换成queryString字符串，并根据参数名按照ASCII码从小到大排序
	 * @param paraMap {@link JSONObject}
	 * @return queryString
	 */
	@SuppressWarnings("unchecked")
	public static String fromJson(JSONObject json) {
		return fromMap(json.toJavaObject(Map.class));
	}
	
	/**
	 * 将{@link String}转换成queryString字符串，并根据参数名按照ASCII码从小到大排序
	 * @param jsonString {@link String}
	 * @return queryString
	 */
	@SuppressWarnings("unchecked")
	public static String fromJsonStr(String jsonString) {
		return fromMap(JSON.parseObject(jsonString, Map.class));
	}


